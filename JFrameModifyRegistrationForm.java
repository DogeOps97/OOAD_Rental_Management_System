    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String input_userID = jTextField1.getText();
        int userID;
        try{
            userID = Integer.parseInt(input_userID);
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter a number for userID", "Alert", JOptionPane.WARNING_MESSAGE);
            return;
        }
        User test = admin.findUserRegistration(userID);
        
        if (test == null){
            JOptionPane.showMessageDialog(null, "Cannot find user with that specified userID", "Alert", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String userName = jTextField2.getText();
       
        String userPassword = jTextField3.getText();
        String userRealName = jTextField4.getText();
        String userContact = jTextField5.getText();
        
        JComboBox j = jComboBox1;
        String userRole = (String) j.getSelectedItem();
        // str.replace(/\s/g, "")
        int returnCode = admin.modifyRegistration(userID, userName, userPassword, userRealName, userContact, userRole);
        
        if (returnCode == 0){
            JOptionPane.showMessageDialog(null, "User registration detail changed successfully");
        }
        
        else if (returnCode == -1){
            JOptionPane.showMessageDialog(null, "Cannot find user with that specified userID", "Alert", JOptionPane.WARNING_MESSAGE);
        }
        
        else if (returnCode == -2){
            JOptionPane.showMessageDialog(null, "username and password can't be left empty", "Alert", JOptionPane.WARNING_MESSAGE);
        }
        
        else if (returnCode == -3){
            JOptionPane.showMessageDialog(null, "Invalid role selected", "Alert", JOptionPane.WARNING_MESSAGE);
        }
        
        else {
             JOptionPane.showMessageDialog(null, "Unknown error has occured", "Alert", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

