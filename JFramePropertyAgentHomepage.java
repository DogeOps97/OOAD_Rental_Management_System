    private void clearPropertyTable(JTable table) {
        DefaultTableModel dtm = (DefaultTableModel) table.getModel();
        dtm.setRowCount(0);
    }
    private void propertyTableSearchResult(JTable table, ArrayList<Property> propertyList) {
        clearPropertyTable(table);
        String col[] = { "ID", "Property Type", "Property Price","Property Size", "Project Name", "Number Of Rooms", "Number Of Bathooms", "Current status", "Address", "Facilities" }; 
        
        DefaultTableModel tableModel = new DefaultTableModel(0, col.length);
        tableModel.setColumnIdentifiers(col);
        table.setModel(tableModel);
        table.setEnabled(false);
        for (Property row : propertyList) {
            int propertyID = row.getPropertyID();
            String propertyType = row.getPropertyType();
            double propertyPrice = row.getPropertyPrice();
            double propertySize = row.getPropertySize();
            String projectName = row.getProjectName();
            // Property current status
            int numOfRooms = row.getNumRoom();
            int numOfBathrooms = row.getNumBathroom();
            int currentStatus = row.getCurrentStatus();
            String status = "Active";
            if (currentStatus == 0) {
                status = "Inactive";
            }
            // address
            String address = row.getAddress().get(1);
            ArrayList<String> facilities = row.getFacilities();
                StringBuilder test_facilities = new StringBuilder();
                for (String facilitiesPart : facilities){
                    test_facilities.append(facilitiesPart);
                    test_facilities.append(",");
                }

            String actualFacilities = test_facilities.toString(); 
            
            Object [] data = {propertyID, propertyType, propertyPrice, propertySize, projectName, numOfRooms,numOfBathrooms,
                status,address,actualFacilities };
            tableModel.addRow(data);
        }
    }
