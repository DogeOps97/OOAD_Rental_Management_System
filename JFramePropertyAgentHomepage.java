    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        // view property details button
        PropertyManager propertylist = PropertyManager.getInstance();
        // check if the field is empty or not integer
        if(propertyIDtoView.getText().equals("") || !isInteger(propertyIDtoView.getText()) ){
            // display error message
            JOptionPane.showMessageDialog(null,"Invalid Property ID!");        
        }
        else{     
            // get propertyID and further check
            int viewPropertyID = Integer.parseInt(propertyIDtoView.getText());
            // this function will return null if the propertyID is not under the property agent
            Property propertyToView = propertylist.findProperty(viewPropertyID);
            // display error
            if(propertyToView == null){
                JOptionPane.showMessageDialog(null,"Property is not in the system!");
            }
            // call frame of editing property
            else{
                this.dispose();
                JFramePropertyAgentHomepageViewDetails a = new JFramePropertyAgentHomepageViewDetails(propertyAgent,propertyToView);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

