    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int userID = owner.getUserID();
        String userName = owner.getUserName();
        String userPassword = owner.getUserPassword();
        String userRealName = jTextFieldRealName.getText().trim().toString();
        String userContact = jTextFieldContactInfo.getText().trim().toString();
        String accountType = owner.getAccountType();
        
        LoginIDManager loginManager = LoginIDManager.getInstance();
        loginManager.modifyUser(userID, userName, userPassword, userRealName, userContact, accountType);
        JOptionPane.showMessageDialog(null, "Completed!");
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed
    
